#!/usr/bin/env python3
# coding=utf-8


def addition(x, y):
    """Compute ``x + y``, i.e., the result of the addition

    .. math::
        :label: eq:my-addition

        x + y

    Args:
        x (number): First number
        y (number): Second number

    Returns:
        number: :math:`x + y`
    """
    return x + y
