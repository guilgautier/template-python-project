#!/usr/bin/env python3
# coding=utf-8


def addition(x, y):
    r"""Compute the addition

    .. math::
        :label: eq:my-addition

        x + y

    :param x: first arguments
    :type x: Number
    :param y: second argument
    :type y: Number
    :return: :math:`x + y`
    :rtype: Number
    """
    return x + y
